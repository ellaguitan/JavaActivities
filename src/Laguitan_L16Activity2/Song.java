package Laguitan_L16Activity2;

public class Song {
	private String sTitle;
	private String sArtitst;
	
	public Song(String sTitle, String sArtitst) {
		super();
		this.sTitle = sTitle;
		this.sArtitst = sArtitst;
	}

	public String getTitle() {
		return sTitle;
	}

	public String getArtist() {
		return sArtitst;
	}	
}
